var color = 'orange';
switch (color) {
    case 'red':
        console.log('color is red');
        break;
    case 'green':
        console.log('color is green');
        break;
    case 'blue':
        console.log('color is blue');
        break;
    case 'gray':
        console.log('color is gray');
        break;
    case 'orange':
        console.log('color is orange');
        break;
    case 'white':
        console.log('color is white');
        break;
    default:
        console.log('color is confusing');
        break;
}