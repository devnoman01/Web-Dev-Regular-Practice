console.log(5 > 6);         //  is 5 greater than 6
console.log(5 < 6);         //  is 5 less than 6
console.log(5 == 6);        //  is 5 equal to  6
console.log(6 == 6);        //  is 6 equal to  6
console.log(6 != 6);        //  is 6 not equal to  6

var myMoney = 100;
var yourMoney = 200;
console.log(myMoney > yourMoney);
console.log(myMoney < yourMoney);
console.log(myMoney == yourMoney);
console.log(myMoney !== yourMoney);

//  and operator
console.log(myMoney && yourMoney);

//  or operator
console.log(myMoney || yourMoney);