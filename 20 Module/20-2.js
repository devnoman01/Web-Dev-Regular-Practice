//  inch to feet conversion
function inchToFeet(inches) {
    var feet = inches / 12;
    return feet;
}
var inchesResult = inchToFeet(132);
console.log(inchesResult);

//  mile to kilometer conversion
function milesToKm(miles) {
    var km = miles * 1.609;
    return km;
}
var kmResult = milesToKm(33);
console.log(kmResult);